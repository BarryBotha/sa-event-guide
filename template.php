                        <h1 class="page-head"><strong>28</strong> handy event planning tips</h1>
                    </div>
                </div>

                <div class="em-row">
                    <div class="em-cell em-cell-12">
                        <article>
                            <h2>Before The Event</h2>
                            <ol>
                                <li><strong>Plan in advance</strong> to avoid last-minute panic.</li>
                                <li><strong>Delegate.</strong> Make your life easier by getting others involved. Having a team to execute the event will help ensure systematic execution. Make a schedule of regular meetings with the team. Clearly identify and communicate on-site responsibility areas for committees and volunteers.</li>
                                <li><strong>Set goals.</strong> Why are you hosting this event? Use a unique selling point and be original and creative in your thoughts and plans.</li>
                                <li><strong>Prepare a preliminary agenda.</strong> Set deadlines for each aspect of the event and add them to your agenda. This will also help you set the criteria for the venue.</li>
                                <li><strong>Determine costs.</strong> How much you will be charged? Get everything in writing.</li>
                                <li><strong>Budget.</strong> Allocate specific funds to every aspect of the event. Consider sponsorships if finance is tight. Regularly review, update, and confirm the event budget.</li>
                                <li><strong>Identify possible dates.</strong> No date will suit everyone, but thinking ahead can limit the inconvenience for all parties.</li>
                                <li><strong>Identify guest list and participants.</strong> It is crucial to have a schedule for what topics will be discussed along with who will lead each segment of the event.</li>
                                <li><strong>Pick a venue.</strong> Whenever possible speak to companies that have hosted similar events. Send event requirements to selected venues with requests for written proposals. If you are signing a contract you should always get a written proposal.</li>
                                <li><strong>Conduct site visits as required.</strong> This is very important the first time you use a facility.</li>
                                <li><strong>Negotiate hotel rates.</strong> If you use a hotel for the event, you should get a significant discount on rooms.</li>
                                <li><strong>Service providers.</strong> Identify needs for outside service providers, and specify requirements. Some key service providers include:
                                <ul>
                                <li>Caterers. Make sure you take any specific dietary requirements into account.</li>
                                <li>Decorators</li>
                                <li>Entertainment</li>
                                <li>Security services</li>
                                <li>Transport requirements</li>
                                <li>Air travel bookings</li>
                                <li>Audio-visual. Obtain audio-visual needs from speakers and presenters. Presentation functionality, internet access and white boards are just some of the components you might need to arrange for. Be proactive in requesting these details, and follow up to confirm them a few days before the meeting.</li>
                                </ul>
                                </li>
                                <li><strong>References.</strong> Obtain references from companies who have used your preferred suppliers. Remember to check if they have adequate staff to handle your event and if they have insurance coverage.</li>
                                <li><strong>Establish your theme and graphics.</strong> Corporate events are great ways to enhance your corporate brand.</li>
                                <li><strong>Select and order</strong> speaker gifts and any awards you may be handing out.</li>
                                <li><strong>Hire a printer</strong> for invitations, agendas, menus, name badges and so on.</li>
                                <li><strong>Invite and confirm</strong> key speakers.</li>
                                <li><strong>Invite guests and keep track of RSVPs.</strong> For out-of-town guests, confirm:
                                <ul>
                                <li>Transportation</li>
                                <li>Hotel Reservations</li>
                                <li>Entertainment</li>
                                <li>Itinerary</li>
                                <li>Transport requirements</li>
                                <li>Maps</li>
                                <li>Welcome gifts or goodie bags.</li>
                                </ul>
                                </li>
                                <li><strong>Admission.</strong> If you are charging admission to the event establish registration-fee structures and policies. Be certain to include clear cancellation policies. Establish a registration process.</li>
                                <li><strong>Do PR for the event.</strong> Calendar notices, press releases and interviews may all be appropriate depending on the size of your event.</li>
                                <li><strong>Send final details.</strong> Approximately seven days before the event you should send a final copy of the itinerary and details to each participant. Also include directions, parking information, room numbers and emergency contacts. If you are working with executives then copy their administrative assistants on the email as well.</li>
                            </ol>
                        </article>
                        <article>
                            <h2>Day of The Event</h2>
                            <ol>
                                <li><strong>Arrive on-site early.</strong> Inspect the location and meet with the service providers. Confirm and monitor pickup of all rental equipment and supplies. Depending on the location, you might also want to post signage.</li>
                                <li><strong>Have a meeting with your team</strong> to review responsibilities, procedures, and overlap areas like registration. It will feel like you don’t have time, but taking 10 minutes for an informational, and motivational, meeting will save you a lot of headaches later that day.</li>
                            </ol>
                        </article>
                        <article>
                            <h2>After The Event</h2>
                            <ol>
                                <li><strong>Pack and inventory all material.</strong> Many of the materials are reusable. It’s a big investment, so take good care of it.</li>
                                <li><strong>Do financial reconciliation.</strong> With a big event, you’ll have many invoices and you need to make sure you have been billed correctly and you pay in a timely fashion. Hold back balance payment till the work is completely done.</li>
                                <li><strong>Prepare a list for thank-you letters and emails</strong>, send them as soon as possible. Perform post-budget performance review. Were you on budget? Could you have saved money?</li>
                                <li><strong>Collect and organise data</strong> for final meeting reports.</li>
                                <li><strong>Obtain evaluations from staff</strong>, volunteers and consultants. It is very important to evaluate what went right and what went not so right so the next event is easier.</li>
                            </ol>
                        </article>
